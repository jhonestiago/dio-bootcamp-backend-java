package escola;

public class Aluno {

    private String nome;
    private int idade;

    // nome.getter
    public String getNome() {
        return nome;
    }

    // nome.setter
    public void setNome(String nome) {
        this.nome = nome;
    }

    // idade.getter
    public int getIdade() {
        return idade;
    }

    // idade.setter
    public void setIdade(int idade) {
        this.idade = idade;
    }

}